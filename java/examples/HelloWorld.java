// Example 1: Basic Hello World
// The simplest Java program

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}

/*
 * Structure:
 * 1. public class HelloWorld - Class declaration (must match filename)
 * 2. public static void main(String[] args) - Entry point
 * 3. System.out.println() - Prints to console with newline
 * 
 * To compile: javac HelloWorld.java
 * To run: java HelloWorld
 */
