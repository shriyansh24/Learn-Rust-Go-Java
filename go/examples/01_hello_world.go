// Example 1: Basic Hello World
// The simplest Go program

package main

import "fmt"

func main() {
    fmt.Println("Hello, World!")
}

/*
 * Structure:
 * 1. package main - Declares this is an executable program
 * 2. import "fmt" - Brings in the format package
 * 3. func main() - Entry point of the program
 * 4. fmt.Println() - Prints with a newline
 */
